                <h1>Ce sunt beneficiile sau ajutoarele sociale?</h1>
                <p>Acestea sunt ajutoare oferite de stat in anumite circumstante persoanelor care indeplinesc anumite criterii de rezidenta si eligibilitate (spre exemplu, in cazul in care au copii, venituri mici sau au ramas fara venit).</p>
                <h1>Care sunt tipurile cele mai intalnite de beneficii?</h1>
                <p>Cele mai intalnite beneficii la care se pot incadra si cetatenii romani sunt Child Benefit, Tax Credits, Disability Living Allowance, Employment and Support Allowance, Job Seeker’s Allowance, Maternity Allowance/Statutory Maternity Pay si Housing Benefit.</p>
                <h1>Ce inseamna fiecare din aceste beneficii?</h1>
                <p>Child Benefit este echivalentul Alocatiei pentru copii care se primeste si in Romania.
Ratele pentru Child Benefit sunt de £20.30 pentru primul copil si £13.40 pentru fiecare din urmatorii copii. Sunt eligibili pentru acest tip de beneficiu doar persoanele care se afla in UK, care au obtinut deja National Insurance Number si care au copii de pana la 16 ani (sau intre 16-20 ani, daca inca studiaza full-time sau sunt inregistrati ca ucenici, in scopul pregatirii profesionale), indiferent daca acesti copii locuiesc in UK sau nu. Persoanele care nu sunt rezidente in UK nu se califica pentru acest tip de beneficiu.</p>
                <h1>Tax Credits are doua   elemente: Working Tax Credit si Child Tax Credits.</h1>
                <p>Working Tax Credits este un ajutor financiar care se da persoanelor care lucreaza employed sau self-employed, dar nu castiga suficient de mult pentru a se intretine. Child Tax Credit este ajutor financiar pentru persoanele care au copii. Daca doriti sa aflati daca va calificati, puteti suna direct la Tax Credits Office la 0345 300 3900.</p>
                <h1>Disability Living Allowance este un ajutor financiar pentru persoanele care sufera de un handicap.</h1>
                <p>Suma primita va fi calculata in functie de incadrarea handicapului iar decizia se face in baza certificatelor medicale sau a unei evaluari medicale la care vi se va cere sa participati. Pentru mai multe informatii, va puteti adresa birourilor independente si gratuite ale Citizen Advice Bureau sau la oficiile Job Centre din zona dvs.</p>
