                <p>Medicii nu exclud că în următoarele zile numărul bolnavilor ar putea creşte.</p>
                <p>„Perioada de incubare a bolii este de până la 45 de zile. Deci pe parcursul unei luni şi jumătate vor mai fi copii bolnavi. La moment starea copiilor este satisfăcătoare, se simt bine”, a declarat pentru Jurnal TV șefa secției Boli infecțioase a spitalului din Strășeni, Svetlana Cerlat.</p>
                <p>Între timp, o echipă de specialişti de la Centrul Naţional de Sănătate Publică a efectuat o vizită în satul Drăguşeni. Medicii infecţionişti au constatat că apa din fântânile din localitate este contaminată cu diferite bacterii. Asta în condiţiile în care sătenii cresc multe oi şi vaci, iar toată mizeria ajunge în fântâni în timpul ploilor. </p>
                <p>Pentru a exclude alte cazuri de îmbolnăvire, fântânile au fost dezinfectate, însă cele din curţile oamenilor rămân pe seama gospodarilor. </p>
                <p>Potrivit Centrului de Sănătate Publică, şi în raionul Călăraşi au fost depistate şase cazuri sporadice de infecţii cu hepatita A, dar nu este vorba despre un focar. </p>

