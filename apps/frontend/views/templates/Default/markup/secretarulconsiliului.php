            <h2 class="prg">Articolul 39. Atribuţiile secretarului</h2>
            <ol class="sec_list">
                <li>
                    <span>Secretarul îndeplineşte, sub autoritatea primarului, următoarele atribuţii de bază:</span>
                    <ul>
                        <li><span>asigură înştiinţarea convocării consiliului local;</span></li>
                        <li><span>participă la şedinţele consiliului local;</span></li>
                        <li><span>avizează proiectele de decizii ale consiliului local şi contrasemnează deciziile;</span></li>
                        <li><span>asigură buna funcţionare a primăriei;</span></li>
                        <li><span> asigură efectuarea lucrărilor de secretariat;</span></li>
                        <li><span>pregăteşte materialele pe marginea problemelor supuse dezbaterii în consiliul local;</span></li>
                        <li><span>comunică şi remite, în termen de 5 zile, dacă legea nu prevede altfel, autorităţilor şi persoanelor interesate actele emise de consiliul local sau de primar;</span></li>
                        <li><span>asigură consultarea publică a proiectelor de decizii ale consiliului local şi de dispoziţii normative ale primarului, informarea publicului referitor la deciziile adoptate şi la dispoziţiile emise;
                                [Art.39 al.(1), lit.h) în redacţia LP72 din 04.05.10, MO94-97/11.06.10 art.270]</span></li>
                        <li><span>primeşte, distribuie şi întreţine corespondenţa;</span></li>
                        <li><span>eliberează extrase sau copii de pe orice act din arhiva consiliului local, în afara celor care conţin informaţii secrete, stabilite potrivit legii, precum şi extrase şi copii de pe actele de stare civilă;</span></li>
                        <li><span>organizează, coordonează, poartă răspundere pentru activitatea serviciilor de stare civilă, de protecţie socială şi exercită, după caz, atribuţiile respective;</span></li>
                        <li><span>ţine evidenţa gospodăriilor ţărăneşti (de fermier) şi a asociaţiilor acestora conform registrului aprobat de Guvern;</span></li>
                        <li><span>păstrează şi aplică, după caz, sigiliul;</span></li>
                        <li><span>acordă ajutor consilierilor şi comisiilor consiliului local;</span></li>
                        <li><span>administrează sediul primăriei şi bunurile ei;</span></li>
                        <li><span>eliberează certificate de proprietate privată ce confirmă dreptul de proprietate asupra cotelor-părţi valorice din patrimoniul fostelor întreprinderi agricole, în baza anexelor la procesul-verbal al comisiei de privatizare a bunurilor întreprinderilor agricole, conform modelului aprobat de Ministerul Economiei şi Comerţului;</span></li>
                        <li><span> îndeplineşte acte notariale conform Legii cu privire la notariat;</span></li>
                    </ul>
                </li>
            </ol>
