                <h1>Sunteţi îngrijorat în privinţa unui copil? </h1>
                <p>Credeţi că un copil are nevoie de ajutor? Doriţi să raportaţi un caz de abuz fizic, emoţional sau de alt fel? Completaţi on-line (Formularul de raportare vezi mai jos) a unui caz de abuz sau vizitaţi pagina Contacte şi contactaţi-ne. </p>
                <h1> Cineva te-a agresat şi ai nevoie de ajutor? Vreai să vorbeşti cu o persoană de încredere? </h1>
                <p>Eşti un copil care are nevoie de ajutor? Mergi la pagina Contacte şi contactează-ne la telefon, prin email, scrie-ne o scrisoare pe adresa noastră, trimite-ne un fax sau un e-mail. Scrie-ne cum să te găsim. </p>
                <h1>Raportaţi un caz de abuz sau neglijare a copilului </h1>
                <p>Sunaţi 902 în cazul dacă viaţa şi securitatea unui copil sunt în pericol eminent. Pentru a raporta o situaţie de abuz sau neglijare a unui copil, puteţi suna la 75.88.06 la Centrul AMICUL. </p>
                <h1>De ce unii oameni nu raportează un abuz asupra copilului? </h1>
                <p>Raportarea unui caz de abuz sau neglijare poate părea o sarcină complicată, descurajantă sau chiar periculoasă. Unii dintre oamenii care cunosc o situaţie de abuz sau neglijare evită să o raporteze. Iată câteva argumente pe care le au în minte atunci când nu raportează: </p>
                <h1>„Nu e treaba mea să mă amestec în familia altora” </h1>
                <p>Este responsabilitatea fiecăruia dintre noi pentru ca orice copil din comunitate sau vecinătate să crească în siguranţă. </p>
                <h1>„Mă tem că copilul va fi luat din familie” </h1>
                <p>Există posibilităţi de a ajuta copilul fără a-l separa pentru totdeauna de familie. În unele situaţii familiile pot fi ajutate de către asistentul social, psiholog sau alt specialis. . În alte cazuri, copilul poate fi referit la un Centru comunitar specializat în prevenirea abuzului sau plasat provizoriu într-un adăpost. Soluţiile pentru fiecare situaţie trebuie să urmărească interesul superior al copilului. </p>
                <h1>„Mă tem de răzbunare”</h1>
                <p>Legea asigură confidenţialitatea persoanei care raportează un caz de abuz sau neglijare a copilului. Persoanele care depun mărturii cu bună credinţă sunt apărate împotriva oricăror acuzaţii. Cel care dezvăluie identitatea persoanei care a raportat cazul este pasibil de pedeapsă civilă sau penală. Organele abilitate cu răspunderea de a primi şi a examina rapoarte despre abuz asupra copilului pot accepta şi declaraţii anonime a cetăţenilor. În acelaşi timp, cei care raportează sunt încurajaţi să lase date de contact pentru cazul în care va fi nevoie de altă informaţie. </p>
                
