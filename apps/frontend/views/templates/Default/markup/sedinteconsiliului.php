            <p>Mai mulți locatari ai blocului de la Botanica a cărui mansardă a ars la începutul lunii mai au venit din nou la Primărie. Ei sunt nemulțumiți de faptul că lucrările de restabilire a acoperișului nu au început încă. Autoritățile municipale dau însă asigurări că la mijlocul lunii septembrie lucrările vor fi finalizate.</p>
            <p>Locatarii blocului situat pe strada Independenței din capitală, a cărui mansardă a fost mistuită de flăcări în luna mai susțin că muncitorii care acum demontează acoperișul vechi nu se grăbesc să finalizeze lucrările. Din această cauză oamenii sunt nevoiți să locuiască în continuare în apartament în care plouă, iar pereţii sunt plini de igrasie:</p>
            <p>"Nu e lucru care ar trebui făcut de oamenii care își cunosc meseria dar sunt niște copii veniți de la țară să lucreze pentru 100 de lei. Cu așa meșteri noi nu o să avem acoperiș".</p>
            <p>"Situația este dezastruoasă. Cât nu am fost două săptămâni, era imposibil să mai stai în casă. Ieri am prelucrat din nou apartamentul cu antimucegai".</p>
            <p>"Le-am scris autorităților municipale un demers. Ni s-a răspuns că termenul de executare a lucrărilor pe acoperiș este de 90 de zile. Vrem să știm cât vor mai dura aceste lucrări și dacă trebuie să sufere oamenii".  </p>
            <p>În cadrul ședinței Primăriei, autoritățile municipale au spus că lucrările de construcție ar putea începe marţi.</p>
            <p>"Agentul economic spune că o să se țină de cuvânt și o să îndeplinească totul conform graficului care l-a semnat. Adică astăzi trebuie să se finalizeze demolarea pentru ca să înceapă a instala construcția", a spus șeful Direcției locativ-comunale, Petru Gontea.   </p>
            <p>Primarul capitalei, Dorin Chirtoacă, le-a cerut responsabililor ca în această săptămână să înceapă și evaluarea pagubelelor produse de incendiu și ploi. Iar şi pe cele suportate de proprietarii apartamentelor situate la etajele de jos: ”Pot începe deja lucrările de reparație, primesc suma pentru compensații și acolo se poate introduce și două trei luni pentru chirie pentru a nu fi încurcate lucrurile”.</p>
            <p>''Pot începe deja lucrările de reparație, primesc suma pentru compensații și acolo se poate introduce și două trei luni pentru chirie pentru a nu fi încurcate lucrurile. Trebuie de început de la cazurile cele mai grave", a spus Dorin Chirtoacă.</p>
            <p>Pentru construcția acoperișului blocului, Primăria a alocat 3,5 milioane de lei.</p>


